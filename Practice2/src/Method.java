
public class Method {

	private int x;
	private String name;
	private double d;
	
	public Method(int x, String name, double d) {
		this.x = x;
		this.name = name;
		this.d = d;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public double getD() {
		return d;
	}

	public void setD(double d) {
		this.d = d;
	}
	

	
}
