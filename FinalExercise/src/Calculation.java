
public class Calculation {
	
	double firstNumber;
	double secondNumber;
	
	public void add() {
		double add = firstNumber + secondNumber;
		System.out.println("The result is: " + add);
	}
	public void substract() {
		double substract = firstNumber - secondNumber;
		System.out.println("The result is: " + substract);
	}
	
	public void multiply() {
		double multiply = firstNumber * secondNumber;
		System.out.println("The result is: " + multiply);
	}
		
	public void divide() {
		double divide = firstNumber / secondNumber;
		System.out.println("The result is: " + divide);
	}
		
	
	}

