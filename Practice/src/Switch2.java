
public class Switch2 {

	public static void main(String[] args) {
		
		int dayNumber = 1;
		
		switch (dayNumber)
		{
		case 0:
			System.out.println("Saturday");
			break;
		case 1:
			System.out.println("Sunday");
			break;
		default:
			System.out.println("another day");
		}

	}

}
