
public class If {

	public static void main(String[] args) {


		int x = 30;
			
			if (x == 7)
			{
				System.out.println("It's a 7.");
			}
			else if (x > 30)
			{
				System.out.println("It's more than 30.");
			}
			else 
			{
				System.out.println("It's more than 7 but less than 30.");
			}
	}

}
