
public class Switch {

	public static void main(String[] args) {
		
		int i = 6;
		
		switch (i)
		{
		case 6:
			System.out.println("The value is 6");
			break;
		case 7:
			System.out.println("The value is 7");
			break;
		default:
			System.out.println("The value is something else");
		}
		

	}

}
